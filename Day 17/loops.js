//print all the odd numbers from (1 to 15)
console.log("\n\nODD NUMBERS");
for(i=1; i<=15; i++){
    if(i%2!=0){
        console.log(`${i} is an odd number\n`);
    }
}

console.log("\n\nEVEN NUMBERS");
//print all the even numbers from (2 to 10)
for(i=1; i<15; i++){
    if(i%2==0){
        console.log(`${i} is an even number\n`);
    }
}